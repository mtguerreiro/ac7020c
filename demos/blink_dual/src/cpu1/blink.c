
#include "stdio.h"

#include "xparameters.h"
#include "xgpiops.h"
#include "sleep.h"

//=============================================================================
/*--------------------------------- Defines ---------------------------------*/
//=============================================================================
#define GPIO_LED_ADDR           XPAR_XGPIOPS_0_BASEADDR
#define GPIO_LED_PIN            0
#define BLINK_PERIOD_MS         2000
//=============================================================================

//=============================================================================
/*--------------------------------- Globals ---------------------------------*/
//=============================================================================
static XGpioPs led;
//=============================================================================

//=============================================================================
/*-------------------------------- Prototypes -------------------------------*/
//=============================================================================
static void mainInitGpio(void);
//=============================================================================

//=============================================================================
/*---------------------------------- Main -----------------------------------*/
//=============================================================================
//-----------------------------------------------------------------------------
int main(void ){

    uint32_t state;

    mainInitGpio();

    state = 0;

    while(1){
        state = state ^ 1;
        XGpioPs_WritePin(&led, GPIO_LED_PIN, state);
        msleep(BLINK_PERIOD_MS);
    }
}
//-----------------------------------------------------------------------------
//=============================================================================

//=============================================================================
/*---------------------------- Static functions -----------------------------*/
//=============================================================================
//-----------------------------------------------------------------------------
static void mainInitGpio(void){

    XGpioPs_Config *ConfigPtr;

    ConfigPtr = XGpioPs_LookupConfig(GPIO_LED_ADDR);
    XGpioPs_CfgInitialize(&led, ConfigPtr, ConfigPtr->BaseAddr);

    XGpioPs_SetDirectionPin(&led, GPIO_LED_PIN, 1);
    XGpioPs_SetOutputEnablePin(&led, GPIO_LED_PIN, 1);
}
//-----------------------------------------------------------------------------
//=============================================================================
