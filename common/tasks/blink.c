//=============================================================================
/*-------------------------------- Includes ---------------------------------*/
//=============================================================================
#include "blink.h"

#include "stdio.h"

/* Kernel */
#include "FreeRTOS.h"
#include "task.h"

/* Device and drivers */
#include "xparameters.h"
#include "xgpio.h"
//=============================================================================

//=============================================================================
/*--------------------------------- Defines ---------------------------------*/
//=============================================================================
#define GPIO_LED_ADDR           XPAR_XGPIO_0_BASEADDR
#define GPIO_LED_CHANNEL        1
#define GPIO_LED                1
#define BLINK_PERIOD_MS         1000
//=============================================================================

//=============================================================================
/*--------------------------------- Globals ---------------------------------*/
//=============================================================================
static XGpio led;
//=============================================================================

//=============================================================================
/*---------------------------------- Task -----------------------------------*/
//=============================================================================
//-----------------------------------------------------------------------------
void blink(void *param){

    (void)param;
    uint32_t state;

    XGpio_Initialize(&led, GPIO_LED_ADDR);
    XGpio_SetDataDirection(&led, GPIO_LED_CHANNEL, 0);
    state = 0;

    while(1){
        state = state ^ 1;
        XGpio_DiscreteWrite(&led, GPIO_LED_CHANNEL, state);
        vTaskDelay(BLINK_PERIOD_MS / portTICK_PERIOD_MS);
    }
}
